import Foundation
import UIKit

extension UIFont {
    class func tstarProRegular(size: CGFloat)-> UIFont {
        return UIFont.init(name: "TSTARPRO-Regular", size: size)!
    }
    
    class func tstarProMedium(size: CGFloat)-> UIFont {
        return UIFont.init(name: "TSTARPRO-Medium", size: size)!
    }
    
    class func tstarProBold(size: CGFloat)-> UIFont {
        return UIFont.init(name: "TSTARPRO-Bold", size: size)!
    }
}
